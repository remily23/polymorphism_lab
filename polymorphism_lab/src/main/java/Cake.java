
public class Cake {

    private String name;
    private String baseFlavour;

    public Cake(String name, String baseFlavour){
        this.name = name;
        this.baseFlavour = baseFlavour;
    }

        public String getName(){
        return this.name;
        }

        public String getBaseFlavour(){
        return this.baseFlavour;
        }

}
